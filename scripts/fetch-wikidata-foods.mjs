// Fetches dishes from Wikidata with English + Thai labels and aliases.
// Usage: node scripts/fetch-wikidata-foods.mjs
//
// Output:
//   - scripts/wikidata-foods.json (raw)
//   - scripts/wikidata-foods.sql  (ready to paste into Supabase SQL Editor)
//
// The query uses P31/P279* walk from Q746549 (dish). Wikidata caps at ~60s,
// so we bound by LIMIT to stay within budget. Re-run if you need more data.

import { writeFileSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT_JSON = `${HERE}/wikidata-foods.json`;
const OUT_SQL = `${HERE}/wikidata-foods.sql`;

const SPARQL = `
SELECT ?dish
       (SAMPLE(?nameEn) AS ?en)
       (SAMPLE(?nameTh) AS ?th)
       (GROUP_CONCAT(DISTINCT ?aliasEn; separator="|") AS ?aliasesEn)
       (GROUP_CONCAT(DISTINCT ?aliasTh; separator="|") AS ?aliasesTh)
WHERE {
  ?dish wdt:P31/wdt:P279* wd:Q746549.
  OPTIONAL { ?dish rdfs:label ?nameEn   FILTER(LANG(?nameEn)   = "en") }
  OPTIONAL { ?dish rdfs:label ?nameTh   FILTER(LANG(?nameTh)   = "th") }
  OPTIONAL { ?dish skos:altLabel ?aliasEn FILTER(LANG(?aliasEn) = "en") }
  OPTIONAL { ?dish skos:altLabel ?aliasTh FILTER(LANG(?aliasTh) = "th") }
  FILTER (BOUND(?nameEn) || BOUND(?nameTh))
}
GROUP BY ?dish
LIMIT 8000
`;

const endpoint = "https://query.wikidata.org/sparql";
const url = `${endpoint}?format=json&query=${encodeURIComponent(SPARQL)}`;

console.log("Querying Wikidata...");
const res = await fetch(url, {
  headers: {
    "User-Agent":
      "FoodRecommendBootstrap/0.1 (https://example.local) node-fetch",
    Accept: "application/sparql-results+json",
  },
});

if (!res.ok) {
  console.error(`Wikidata returned ${res.status}: ${await res.text()}`);
  process.exit(1);
}

const json = await res.json();
const bindings = json.results.bindings;

const rows = bindings
  .map((b) => {
    const qid = b.dish.value.replace("http://www.wikidata.org/entity/", "");
    const en = b.en?.value?.trim() || null;
    const th = b.th?.value?.trim() || null;
    const aliasesEn = b.aliasesEn?.value
      ? b.aliasesEn.value.split("|").map((s) => s.trim()).filter(Boolean)
      : [];
    const aliasesTh = b.aliasesTh?.value
      ? b.aliasesTh.value.split("|").map((s) => s.trim()).filter(Boolean)
      : [];

    // Drop garbage rows.
    if (!en && !th) return null;
    // Drop entries that look like Q-ids (no human label resolved).
    if (en && /^Q\d+$/.test(en)) return null;
    return { qid, name_en: en, name_th: th, aliases_en: aliasesEn, aliases_th: aliasesTh };
  })
  .filter(Boolean);

// Deduplicate by qid.
const byQid = new Map();
for (const r of rows) byQid.set(r.qid, r);
const deduped = [...byQid.values()];

mkdirSync(HERE, { recursive: true });
writeFileSync(OUT_JSON, JSON.stringify(deduped, null, 2));

const sqlEscape = (s) => s.replace(/'/g, "''");
const arrLiteral = (arr) =>
  arr.length === 0
    ? "ARRAY[]::text[]"
    : `ARRAY[${arr.map((s) => `'${sqlEscape(s)}'`).join(",")}]::text[]`;

const lines = [
  "-- Generated by scripts/fetch-wikidata-foods.mjs",
  "-- Run after supabase/01_foods.sql has created the foods table.",
  "BEGIN;",
];

const BATCH = 500;
for (let i = 0; i < deduped.length; i += BATCH) {
  const slice = deduped.slice(i, i + BATCH);
  lines.push(
    "INSERT INTO public.foods (wikidata_id, name_en, name_th, aliases, source, status) VALUES",
  );
  const values = slice.map((r) => {
    const aliases = [...new Set([...r.aliases_en, ...r.aliases_th])];
    return `('${r.qid}', ${r.name_en ? `'${sqlEscape(r.name_en)}'` : "NULL"}, ${
      r.name_th ? `'${sqlEscape(r.name_th)}'` : "NULL"
    }, ${arrLiteral(aliases)}, 'wikidata', 'approved')`;
  });
  lines.push(values.join(",\n") + " ON CONFLICT (wikidata_id) DO NOTHING;");
}

lines.push("COMMIT;");
writeFileSync(OUT_SQL, lines.join("\n"));

console.log(`Saved ${deduped.length} dishes`);
console.log(`  JSON: ${OUT_JSON}`);
console.log(`  SQL:  ${OUT_SQL}`);
